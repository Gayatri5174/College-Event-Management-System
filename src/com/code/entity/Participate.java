package com.code.entity;

public class Participate {

	private String stName;
	private String enrollNumber;
	private String gender;
	private String mobile;
	private String clgName;
	private String paymentSts;
	private String regSts;
	private String regDate;
	public String getStName() {
		return stName;
	}
	public void setStName(String stName) {
		this.stName = stName;
	}
	public String getEnrollNumber() {
		return enrollNumber;
	}
	public void setEnrollNumber(String enrollNumber) {
		this.enrollNumber = enrollNumber;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getMobile() {
		return mobile;
	}
	public void setMobile(String mobile) {
		this.mobile = mobile;
	}
	public String getClgName() {
		return clgName;
	}
	public void setClgName(String clgName) {
		this.clgName = clgName;
	}
	public String getPaymentSts() {
		return paymentSts;
	}
	public void setPaymentSts(String paymentSts) {
		this.paymentSts = paymentSts;
	}
	public String getRegSts() {
		return regSts;
	}
	public void setRegSts(String regSts) {
		this.regSts = regSts;
	}
	public String getRegDate() {
		return regDate;
	}
	public void setRegDate(String regDate) {
		this.regDate = regDate;
	}
	
	
	
	
}
