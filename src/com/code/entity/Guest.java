package com.code.entity;

public class Guest {

	private String gId;
	private String gName;
	private String gender;
	private String aboutGuest;
	private String gEmail;
	private String gMobile;
	private String status;
	
	public String getgId() {
		return gId;
	}
	public void setgId(String gId) {
		this.gId = gId;
	}
	public String getgName() {
		return gName;
	}
	public void setgName(String gName) {
		this.gName = gName;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getAboutGuest() {
		return aboutGuest;
	}
	public void setAboutGuest(String aboutGuest) {
		this.aboutGuest = aboutGuest;
	}
	public String getgEmail() {
		return gEmail;
	}
	public void setgEmail(String gEmail) {
		this.gEmail = gEmail;
	}
	public String getgMobile() {
		return gMobile;
	}
	public void setgMobile(String gMobile) {
		this.gMobile = gMobile;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}

	
}
